export const prepareInstructions = ({
  jobTitle,
  jobDescription,
  strict = false,
}: {
  jobTitle: string;
  jobDescription: string;
  strict?: boolean;
}) => {
  const strictnessNote = strict
    ? `\nIMPORTANT: A previous response could not be parsed. Return ONLY the JSON object — no markdown fences, no commentary, no leading or trailing text. The first character of your response must be "{" and the last must be "}".\n`
    : "";

  return `You are an expert in ATS (Applicant Tracking System) analysis and resume review. Analyze the attached resume against the role below and return structured feedback.

Be honest. Score low if the resume is weak — the goal is to help the candidate improve, not flatter them. Use the job description (if provided) to tailor your feedback.

Job title: ${jobTitle || "(not specified)"}
Job description: ${jobDescription || "(not provided)"}
${strictnessNote}
OUTPUT RULES:
- Return EXACTLY one JSON object that matches the schema below.
- No markdown code fences (no \`\`\`json, no \`\`\`).
- No prose before or after the JSON.
- The response must start with "{" and end with "}".
- Every \`score\` is an integer 0-100.
- Every \`tips\` array contains 3-4 items.
- \`type\` is exactly \`"good"\` (a strength) or \`"improve"\` (a weakness).

Schema:
${AIResponseFormat}`;
};